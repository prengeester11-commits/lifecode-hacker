# 대운 방향 결정 기준
# (연간_음양, 성별) -> 순행(True)/역행(False)
# 양년 남자 / 음년 여자 = 순행
# 음년 남자 / 양년 여자 = 역행
def get_daewoon_direction(year_stem, gender):
    year_is_yang = (CHEONGAN_UMYANG[year_stem] == 1)
    is_male = (gender == 'M')
    return (year_is_yang and is_male) or (not year_is_yang and not is_male)

