# 월별 절기 목록 (이 달에 해당하는 절기)
MONTH_SOLAR_TERM = {
    1: '소한',  2: '입춘',  3: '경칩',  4: '청명',
    5: '입하',  6: '망종',  7: '소서',  8: '입추',
    9: '백로', 10: '한로', 11: '입동', 12: '대설',
}


def get_solar_term_date(year: int, term_name: str):
    """특정 연도의 절기 날짜 반환 (month, day)"""
    if year in SOLAR_TERMS_TABLE and term_name in SOLAR_TERMS_TABLE[year]:
        return SOLAR_TERMS_TABLE[year][term_name]
    # 테이블에 없으면 기본값 사용
    return SOLAR_TERM_DEFAULT.get(term_name, (1, 1))


def get_month_branch(year: int, month: int, day: int) -> int:
    """
    주어진 날짜의 월지(月支)를 반환 (0=자, 1=축, 2=인, ...)
    절기를 기준으로 월을 판별
    """
    # 이 달의 절기 날짜 찾기
    term_name = MONTH_SOLAR_TERM.get(month)
    if not term_name:
        return 0

    term_month, term_day = get_solar_term_date(year, term_name)
    term_date = date(year, term_month, term_day)
    birth_date = date(year, month, day)

    if birth_date >= term_date:
        # 해당 절기 이후 → 이 달의 월지
        return SOLAR_TERM_BRANCH[term_name]
    else:
        # 절기 이전 → 이전 달의 월지
        prev_month = month - 1 if month > 1 else 12
        prev_term_name = MONTH_SOLAR_TERM.get(prev_month)
        if prev_term_name:
            return SOLAR_TERM_BRANCH[prev_term_name]
        return 11  # 해월로 기본값


def get_year_for_saju(year: int, month: int, day: int) -> int:
    """입춘 기준으로 사주에서 사용할 년도 반환"""
    term_month, term_day = get_solar_term_date(year, '입춘')
    ipchun = date(year, term_month, term_day)
    birth = date(year, month, day)
    if birth < ipchun:
        return year - 1
    return year


def get_nearest_solar_terms(year: int, month: int, day: int):
    """
    태어난 날짜 기준으로 이전 절기와 다음 절기를 반환
    대운 계산에 사용
    Returns: (prev_term_date, next_term_date, prev_term_name, next_term_name)
    """
    birth = date(year, month, day)

    # 전체 절기 목록 (현재 연도 ± 1년)
    all_terms = []
    for y in [year - 1, year, year + 1]:
        for term_name, branch in SOLAR_TERM_BRANCH.items():
            m, d = get_solar_term_date(y, term_name)
            try:
                term_date = date(y, m, d)
                all_terms.append((term_date, term_name))
            except ValueError:
                pass

    all_terms.sort(key=lambda x: x[0])

    prev_term = None
    next_term = None

    for i, (term_date, term_name) in enumerate(all_terms):
        if term_date <= birth:
            prev_term = (term_date, term_name)
        elif next_term is None and term_date > birth:
            next_term = (term_date, term_name)
            break

    return prev_term, next_term
